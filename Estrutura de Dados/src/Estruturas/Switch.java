package Estruturas;

public class Switch {
	public static void main (String args[]) {
		int i = 2;
		switch (i) {
			case 1: System.out.println("opc 1"); break;
			case 2: System.out.println("opc 2"); break;
			case 3: System.out.println("opc 3"); break;
			default: System.out.println("opc 4"); break; 
		}
	}
}
